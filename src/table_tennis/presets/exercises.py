EXERCISE_NAMES = tuple(_BUILDERS)


def build_exercise(name: str, cycles: int = 3) -> ExerciseDefinition:
    if cycles < 3:
        raise ValueError("Exercise videos require at least three cycles.")
    try:
        title, builder = _BUILDERS[name]
    except KeyError as exc:
        raise ValueError(f"Unknown exercise: {name}") from exc
    return ExerciseDefinition(
        name=name,
        title=title,
        cycles=cycles,
        strokes=builder(cycles),
    )


def build_exercises(
    names: list[str] | tuple[str, ...] | None = None,
    cycles: int = 3,
) -> tuple[ExerciseDefinition, ...]:
    selected = EXERCISE_NAMES if names is None else tuple(names)
    return tuple(build_exercise(name, cycles=cycles) for name in selected)


