def _third_ball_hit_and_pass(
    cycles: int,
    wing: str,
) -> tuple[ExerciseStroke, ...]:
    pattern = list(_hit_and_pass(cycles, wing))
    pattern[0] = replace(
        pattern[0],
        contact_moment=2,
        contact_fraction=0.85 if wing == "forehand" else 0.45,
        label="Topspin de tercera bola",
    )
    return (
        _stroke(
            "near",
            "backhand",
            "serve",
            "elbow",
            moment=4,
            fraction=0.55,
            label="Servicio cortado corto",
            service_preset="short",
        ),
        _stroke(
            "far",
            "backhand",
            "push",
            wing,
            moment=4,
            fraction=0.55,
            label=f"Recepción cortada larga al {wing}",
        ),
        *pattern,
    )


