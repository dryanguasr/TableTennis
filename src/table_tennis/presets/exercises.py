def _stroke(
    hitter: str,
    wing: str,
    kind: str,
    target_wing: str,
    *,
    pace: str = "regular",
    moment: int = 2,
    fraction: float = 0.45,
    cycle: int = 0,
    label: str = "",
    contact_wing: str | None = None,
    service_preset: str | None = None,
    bounce_tolerance_mm: float | None = None,
    depth: str = "long",
    opening_attack: bool = False,
) -> ExerciseStroke:
    resolved_bounce_tolerance = (
        550.0
        if bounce_tolerance_mm is None
        and kind in {"drive", "topspin"}
        else 400.0
        if bounce_tolerance_mm is None
        and kind == "block"
        else 400.0
        if bounce_tolerance_mm is None
        and kind == "chop"
        else 150.0
        if bounce_tolerance_mm is None
        else bounce_tolerance_mm
    )
    stroke = ExerciseStroke(
        hitter=hitter,  # type: ignore[arg-type]
        wing=wing,  # type: ignore[arg-type]
        kind=kind,  # type: ignore[arg-type]
        target_wing=target_wing,  # type: ignore[arg-type]
        contact_wing=contact_wing,  # type: ignore[arg-type]
        pace=pace,  # type: ignore[arg-type]
        contact_moment=moment,  # type: ignore[arg-type]
        contact_fraction=fraction,
        cycle=cycle,
        label=label,
        service_preset=service_preset,
        bounce_tolerance_mm=resolved_bounce_tolerance,
        depth=depth,  # type: ignore[arg-type]
        opening_attack=opening_attack,
    )
    if stroke.kind != "serve":
        theoretical_moment, theoretical_fraction = theoretical_contact(
            stroke
        )
        stroke = replace(
            stroke,
            contact_moment=theoretical_moment,
            contact_fraction=theoretical_fraction,
        )
    return stroke


def _drive_to_drive(cycles: int) -> tuple[ExerciseStroke, ...]:
    strokes = []
    for index in range(max(5, cycles)):
        cycle = index + 1
        strokes.extend(
            (
                _stroke(
                    "near",
                    "forehand",
                    "drive",
                    "forehand",
                    cycle=cycle,
                    label="Drive cruzado A",
                ),
                _stroke(
                    "far",
                    "forehand",
                    "drive",
                    "forehand",
                    cycle=cycle,
                    label="Drive cruzado B",
                ),
            )
        )
    return tuple(strokes)


