def _third_ball_change_of_pace(
    cycles: int,
    wing: str,
) -> tuple[ExerciseStroke, ...]:
    wing_label = "drive" if wing == "forehand" else "revés"
    strokes = [
        _stroke(
            "near",
            "backhand",
            "serve",
            wing,
            moment=2,
            fraction=0.35,
            label=f"Servicio cortado largo al {wing_label}",
            service_preset=f"long_{wing}",
        ),
        _stroke(
            "far",
            wing,
            "push",
            wing,
            moment=2,
            fraction=0.45,
            label=f"Recepción cortada larga al {wing_label}",
        ),
        _stroke(
            "near",
            wing,
            "topspin",
            wing,
            pace="strong",
            opening_attack=True,
            label=f"Topspin de tercera bola de {wing_label}",
        ),
        _stroke(
            "far",
            wing,
            "block",
            wing,
            label="Bloqueo",
        ),
    ]
    chopper = "near"
    for cycle in range(1, cycles + 1):
        attacker = "far" if chopper == "near" else "near"
        strokes.extend(
            (
                _stroke(
                    chopper,
                    wing,
                    "chop",
                    wing,
                    moment=4,
                    fraction=0.55,
                    cycle=cycle,
                    label=f"Corte defensivo de {wing_label}",
                ),
                _stroke(
                    attacker,
                    wing,
                    "topspin",
                    wing,
                    pace="strong",
                    opening_attack=True,
                    cycle=cycle,
                    label=f"Topspin de {wing_label}",
                ),
                _stroke(
                    chopper,
                    wing,
                    "block",
                    wing,
                    cycle=cycle,
                    label="Bloqueo y cambio de roles",
                ),
            )
        )
        chopper = attacker
    return tuple(strokes)


